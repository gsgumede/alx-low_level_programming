#include "3-calc.h"

/**
 * op_add - add two numbers
 *
 * @a: integer to be added
 * @b: integer to be added
 * Return: sum of two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 *
 * @a: integer to be subtracted
 * @b: integer to be subtracted
 * Return: difference of two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 *
 * @a: integer to be multiplied
 * @b: integer to be multiplied
 * Return: product of two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 *
 * @a: integer to be divided
 * @b: integer to be divided
 * Return: dividion of two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder two numbers
 *
 * @a: integer to get remainder
 * @b: integer to get remainder
 * Return: remainder of the division of two integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}

